# Arithmetic operators
x = 10
y = 5

addition = x + y
subtraction = x - y
multiplication = x * y
division = x / y
floor_division = x // y
modulus = x % y
exponentiation = x ** y

print(f"Arithmetic Operators:")
print(f"Addition: {addition}")
print(f"Subtraction: {subtraction}")
print(f"Multiplication: {multiplication}")
print(f"Division: {division}")
print(f"Floor Division: {floor_division}")
print(f"Modulus: {modulus}")
print(f"Exponentiation: {exponentiation}\n")

# Comparison operators
a = 10
b = 20

equal = a == b
not_equal = a != b
greater_than = a > b
less_than = a < b
greater_than_or_equal = a >= b
less_than_or_equal = a <= b

print(f"Comparison Operators:")
print(f"Equal: {equal}")
print(f"Not Equal: {not_equal}")
print(f"Greater Than: {greater_than}")
print(f"Less Than: {less_than}")
print(f"Greater Than or Equal: {greater_than_or_equal}")
print(f"Less Than or Equal: {less_than_or_equal}\n")

# Logical operators
p = True
q = False

logical_and = p and q
logical_or = p or q
logical_not = not p

print(f"Logical Operators:")
print(f"Logical AND: {logical_and}")
print(f"Logical OR: {logical_or}")
print(f"Logical NOT: {logical_not}\n")

# Assignment operators
z = 5
z += 3  # Equivalent to z = z + 3
print(f"Assignment Operator (+=): {z}\n")

# Membership operators
fruits = ['apple', 'banana', 'orange']

is_in = 'banana' in fruits
is_not_in = 'grape' not in fruits

print(f"Membership Operators:")
print(f"'banana' is in fruits: {is_in}")
print(f"'grape' is not in fruits: {is_not_in}")
